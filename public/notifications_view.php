    if (!verify_csrf_token($_POST["csrf_token"] ?? null)) {
        flash("error", "Invalid CSRF token.");
    } else {
        $id = (int) ($_POST["id"] ?? 0);
        $isRead = (int) ($_POST["is_read"] ?? 0);
        $stmt = $mysqli->prepare("UPDATE notifications SET is_read = ? WHERE id = ? AND user_id = ?");
        $stmt->bind_param("iii", $isRead, $id, $user["id"]);
        $stmt->execute();
        $stmt->close();
        flash("success", "Notification status updated.");
    }
    header("Location: " . url("notifications_view.php"));
    exit;
}

