setInterval(async () => {
  try {
    const r = await fetch('<?= e(url("notifications.php")) ?>');
    const data = await r.json();
    if (Array.isArray(data.notifications)) {
      const unread = data.notifications.filter(n => String(n.is_read) === "0").length;
      document.title = unread > 0 ? `(${unread}) Notifications` : "Notifications";
    }
  } catch (e) {}
}, 10000);
